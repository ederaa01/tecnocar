    public void salvar() {

        if (jTfNome.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Nome Descricao e obrigatorio!");
            jTfNome.grabFocus();
        } else if (jTfNomeMarca.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Descricao Marca e obrigatorio");
        } else {
            modelo.setNome(jTfNome.getText());
            marca = (Marca) Main.dao.buscaID(Marca.class, jTfCodigoMarca.getText());
            modelo.setMarca(marca);
            try {
                if (jTfCodigo.getText().isEmpty()) {
                    Main.dao.insere(modelo);
                } else {
                    Main.dao.atualiza(modelo);
                }
                dispose();
            } catch (Exception ex) {
                Logger.getLogger(CadModelo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    public void excluir() {
        if (estado == Rotinas.PESQUISANDO || !jTfCodigo.getText().equals("")) {
            if (Rotinas.confirma("Confirma exclusão do Modelo?") == JOptionPane.YES_NO_OPTION) {
                try {
                    Main.dao.remove(modelo);
                    JOptionPane.showMessageDialog(null, "Excluído com Sucesso!");
                    estado = Rotinas.PADRAO;
                    hab_des.botaoCancelar(jPCadastro, jPBtn);
                } catch (Exception ex) {
                    Logger.getLogger(CadModelo.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Faça uma pesquisa para poder excluir!");
        }
    }
