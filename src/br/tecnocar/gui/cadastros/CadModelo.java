    private void jTfCodigoMarcaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTfCodigoMarcaFocusLost
        // TODO add your handling code here:
        marca = (Marca) Main.dao.buscaID(Marca.class, jTfCodigoMarca.getText());
        if (marca == null && !jTfCodigoMarca.getText().trim().equals("")) {
            //JOptionPane.sohow
            JOptionPane.showMessageDialog(null, "Nao encontrou a marca digitada!");
            jTfCodigoMarca.setText("");
            jTfNomeMarca.setText("");
            jTfCodigoMarca.grabFocus();
        } else {
            jTfNomeMarca.setText(marca.getNome());
        }
    }//GEN-LAST:event_jTfCodigoMarcaFocusLost

