    public void salvar() {
        if (jTfNome.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Descricao e Obrigatorio");
            jTfNome.grabFocus();
        } else {

            marca.setNome(jTfNome.getText());
            try {
                if (jTfCodigo.getText().isEmpty()) {
                    Main.dao.insere(marca);
                } else {
                    Main.dao.atualiza(marca);
                }
                dispose();
            } catch (Exception ex) {
                Logger.getLogger(CadMarca.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
