    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
        if (estado == Rotinas.PESQUISANDO || !jTfCodigo.getText().equals("")) {
            if (Rotinas.confirma("Confirma exclusão do Modelo?") == JOptionPane.YES_NO_OPTION) {
                try {
                    Main.dao.remove(marca);
                    JOptionPane.showMessageDialog(null, "Excluído com Sucesso!");
                    estado = Rotinas.PADRAO;
                    hab_des.botaoCancelar(jPCadastro, jPBtn);
                } catch (Exception ex) {
                    Logger.getLogger(CadModelo.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {

            JOptionPane.showMessageDialog(null, "Faça uma pesquisa para poder excluir!");
        }
    }//GEN-LAST:event_jBtnExcluirActionPerformed

